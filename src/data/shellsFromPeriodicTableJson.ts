export function getAtomShellData(atomicNumber: number): {
  shells: readonly number[]
  massNumber: number
} | null {
  const shells = SHELL_ELECTRONS_BY_ATOMIC_NUMBER[atomicNumber]
  if (!shells) return null
  const massNumber = REFERENCE_MASS_NUMBER_BY_Z[atomicNumber] ?? atomicNumber
  return { shells, massNumber }
}
